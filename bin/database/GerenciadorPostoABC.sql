CREATE TABLE "TANQUES" (
	"ID"	INTEGER NOT NULL UNIQUE,
	"DESCRICAO"	VARCHAR(50) NOT NULL,
	"VALOR_LITRO"	NUMERIC(9,3) NOT NULL,
	PRIMARY KEY("ID" AUTOINCREMENT)
)

CREATE TABLE "BOMBAS" (
	"ID"	INTEGER NOT NULL UNIQUE,
	"NUMERO_BOMBA"	INTEGER NOT NULL UNIQUE,
	"ID_TANQUE"	INTEGER NOT NULL,
	PRIMARY KEY("ID" AUTOINCREMENT),
	FOREIGN KEY("ID_TANQUE") REFERENCES "TANQUES"("ID")
)

CREATE TABLE "ABASTECIMENTOS" (
	"ID"	INTEGER NOT NULL UNIQUE,
	"ID_BOMBA"	INTEGER NOT NULL,
	"DATA_ABASTECIMENTO"	TEXT NOT NULL,
	"VALOR_LITRO"	NUMERIC(9,3) NOT NULL,
	"QUANTIDADE_LITROS"	NUMERIC(9,3) NOT NULL,
	"VALOR_IMPOSTO"	NUMERIC(12,2),
	"VALOR_ABASTECIDO"	NUMERIC(12,2),
	PRIMARY KEY("ID" AUTOINCREMENT),
	FOREIGN KEY("ID_BOMBA") REFERENCES "BOMBAS"("ID")
)
